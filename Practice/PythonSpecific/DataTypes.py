"""
Data Types in Python :

    Text Type : str
    Numeric Type : int, float, complex
    Sequence Type : list, tuple, range
    Mapping Type : dict
    Set Type : set, frozenset
    Boolean Type : bool
    Binary Type : bytes, bytearray, memoryview   --> Not required for Automation...

    Set --> Mutable...
    FrozenSet --> Immutable...

    Pending --> complex, range, frozenset...
"""

print(type("Sabari Vivek"))
print(type(21))
print(type(21.8))
print(type(["Sabari", "Vivek"]))
print(type(("Sabari", "Vivek")))
print(type({"Name": "Sabari", "Location": "Chennai"}))
print(type({"Sabari", "Vivek"}))
print(type(frozenset({"Sabari", "Vivek"})))
print(type(True))
