# Usage in In...
list_values = [1, 2, 3, 4, 5]
tuple_values = (1, 2, 3, 4, 5)
set_values = {1, 2, 3, 4, 5}
dictionary_values = {"one": 1, "two": 2, "three": 3, "four": 4, "five": 5}

print(2 in list_values)
print(8 not in tuple_values)
print(4 in set_values)
print("two" in dictionary_values)
