# CalculatorModule.py

def add(a, b):
    print("The addition of {} and {} number is :".format(a, b), (a + b))


def sub(a, b):
    print("The subtraction of {} and {} number is :".format(a, b), (a - b))


def multiply(a, b):
    print("The multiplication of {} and {} number is :".format(a, b), (a * b))


def division(a, b):
    print("The division of {} and {} number is :".format(a, b), (a / b))
